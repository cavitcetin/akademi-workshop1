public class doWhileAndWhile {
    public static void main(String[] args) {

        //DoWhile
        int count = 1;

        do {
            System.out.println("Count: " + count);
            count++;
        } while (count <= 5);


        //While
        int count1 = 1;

        do {
            System.out.println("Count: " + count1);
            count1++;
        } while (count1 <= 5);

    }
}